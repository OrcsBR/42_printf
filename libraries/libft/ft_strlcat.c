#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	srclen;
	size_t	destlen;

	srclen = ft_strlen(src);
	destlen = 0;
	while (dest[destlen] && destlen < size)
		destlen++;
	if (destlen == size)
		return (destlen + srclen);
	i = 0;
	while (src[i] && i + destlen < size - 1)
	{
		dest[destlen + i] = src[i];
		i++;
	}
	dest[destlen + i] = '\0';
	return (destlen + srclen);
}
